Screen('Preference', 'Verbosity', 1);

scrn = PsychWindow(0, true,...
                   'color', [255 255 255],...
                   'rect', [0 0 300 300], ...
                   'alpha_blending', true);

% It seems like only single arcs are allowed?
arcs = Arc('fill_color', [25 15 160 200].',...
           'frame_color', [150 150 150 180].', ...
           'rect', [20 20 160 160].', ...
           'pen_width', 4, ...
           'start_angle', 40, ...
           'arc_angle', 120);

arcs.Draw(scrn.Get('pointer'));
scrn.Flip;

KbWait;
scrn.Close;
